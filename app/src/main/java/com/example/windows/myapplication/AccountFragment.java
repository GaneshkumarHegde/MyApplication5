    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View view = inflater.inflate(R.layout.fragment_account, container, false);
        FirebaseUser user = FirebaseAuth.getInstance().getCurrentUser();
        if (user != null) {
            // Name, email address, and profile photo Url
            String name = user.getDisplayName();
            String email = user.getEmail();
            Uri photoUrl = user.getPhotoUrl();

            // Check if user's email is verified
            boolean emailVerified = user.isEmailVerified();

            // The user's ID, unique to the Firebase project. Do NOT use this value to
            // authenticate with your backend server, if you have one. Use
            // FirebaseUser.getToken() instead.
            String uid = user.getUid();
            TextView textView= (TextView)view.findViewById(R.id.accInfo);
            textView.setText(name+"\n"+email+"\n"+uid);
        }
        else
        {   GoogleSignInOptions gso = new GoogleSignInOptions.Builder(GoogleSignInOptions.DEFAULT_SIGN_IN)
                .requestEmail()
                .build();
            mGoogleSignInClient = GoogleSignIn.getClient(getActivity(), gso);
            Intent signInIntent = mGoogleSignInClient.getSignInIntent();
            Task<GoogleSignInAccount> task = GoogleSignIn.getSignedInAccountFromIntent(signInIntent);
            try {
                GoogleSignInAccount account = task.getResult(ApiException.class);
                TextView textView= (TextView)view.findViewById(R.id.accInfo);
                textView.setText(account.getDisplayName()+"\n"+account.getEmail()+"\n"+account.getId());
                // Signed in successfully, show authenticated UI.

            } catch (ApiException e) {
                // The ApiException status code indicates the detailed failure reason.
                // Please refer to the GoogleSignInStatusCodes class reference for more information.
                Log.w(TAG, "signInResult:failed code=" + e.getStatusCode());
                TextView textView= (TextView)view.findViewById(R.id.accInfo);
                textView.setText("No data");
            }
        }


        return view;
    }

